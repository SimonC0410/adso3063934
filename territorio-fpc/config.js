// IMPORTANTE:
// "localhost" NO funciona desde un celular físico ni siempre desde el
// emulador. Tienes que poner la IP de tu computador en la red local
// (la misma red WiFi donde tienes el celular con Expo Go).
//
// Cómo encontrarla:
//   Windows -> abre cmd y corre: ipconfig          (busca "IPv4 Address")
//   Mac/Linux -> corre: ifconfig  (o `ip addr`)     (busca "inet ...")
//
// Ejemplo: si tu PC tiene la IP 192.168.1.50 y el backend corre en el
// puerto 3000 (como en tu server.js), deja esto así:
//
//   export const API_URL = 'http://192.168.1.50:3000';
//
// Si vas a probar SOLO en el emulador de Android Studio, puedes usar:
//   export const API_URL = 'http://10.0.2.2:3000';

